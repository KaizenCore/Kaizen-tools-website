export function calculatePrice(
    basePrice: [number, number],
    heroDiscount: boolean,
    curedDiscount: boolean,
): [number, number] {
    let multiplier = 1;

    if (curedDiscount && heroDiscount) {
        multiplier = 1 - discountInfo.zombieVillagerCure.stackedDiscount;
    } else if (curedDiscount) {
        multiplier = 1 - discountInfo.zombieVillagerCure.discount;
    } else if (heroDiscount) {
        multiplier = 1 - discountInfo.heroOfTheVillage.discount;
    }

    return [Math.max(1, Math.floor(basePrice[0] * multiplier)), Math.max(1, Math.floor(basePrice[1] * multiplier))];
}

