export function getBiomesByDimension(dimension: 'overworld' | 'nether' | 'end'): BiomeData[] {
    return biomes.filter((biome) => biome.dimension === dimension);
}

export function getBiomesByCategory(category: string): BiomeData[] {
    if (category === 'all') {
        return biomes;
    }
    return biomes.filter((biome) => biome.category === category);
}

export function searchBiomes(query: string): BiomeData[] {
    const lowerQuery = query.toLowerCase();
    return biomes.filter(
        (biome) =>
            biome.name.toLowerCase().includes(lowerQuery) ||
            biome.category.toLowerCase().includes(lowerQuery) ||
            biome.structures.some((s) => s.toLowerCase().includes(lowerQuery)) ||
            biome.blocks.some((b) => b.toLowerCase().includes(lowerQuery)) ||
            biome.features.some((f) => f.toLowerCase().includes(lowerQuery))
    );
}

export function getTemperatureLabel(temperature: number): string {
    if (temperature < 0) {
        return 'Freezing';
    }
    if (temperature < 0.5) {
        return 'Cold';
    }
    if (temperature < 1.0) {
        return 'Temperate';
    }
    if (temperature < 1.5) {
        return 'Warm';
    }
    return 'Hot';
}

export function getRainfallLabel(rainfall: number): string {
    if (rainfall === 0.0) {
        return 'None';
    }
    if (rainfall < 0.3) {
        return 'Low';
    }
    if (rainfall < 0.7) {
        return 'Medium';
    }
    return 'High';
}
