export type ItemCategory =
    | 'tools'
    | 'weapons'
    | 'armor'
    | 'food'
    | 'building'
    | 'redstone'
    | 'decoration'
    | 'materials'
    | 'potions'
    | 'spawn_eggs'
    | 'special';

export type EnchantmentCategory = 'protection' | 'damage' | 'utility' | 'curse';

export type AttributeModifierOperation =
    | 'add_value'
    | 'add_multiplied_base'
    | 'add_multiplied_total';

export type EquipmentSlot =
    | 'mainhand'
    | 'offhand'
    | 'head'
    | 'chest'
    | 'legs'
    | 'feet'
    | 'any';

export type Rarity = 'common' | 'uncommon' | 'rare' | 'epic';

export type HideFlag =
    | 'enchantments'
    | 'modifiers'
    | 'unbreakable'
    | 'can_destroy'
    | 'can_place'
    | 'other'
    | 'dyed'
    | 'armor_trim';

